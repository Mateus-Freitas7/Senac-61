


numero = []
for i in range (10):
 n1 = int(input("digite um num:"))
 numero.append(n1)

print("O maior numero é:",max(numero)) #Max = número maior
print("O menor número é:",min(numero)) #Min =  menor
print(f"Soma:{sum(numero)}")           #Sum =Soma de tudo
print(f"Média:{sum(numero)/len(numero)}") #Len = quantidade de itens numa lista



